:Bash :Shortuts

* "Control + a" Move To Beginning Of Line

* "Control + e" Move To End Of Line

* "Option + ->" Move To Left Per Word

* "Option + <-" Move To Right Per Word

* "Option + Delete" Delete By Word

* "Control + k" Delete Till End

* "Control + u" Delete TIll Beginning 

:Bash :Alias

* "chbash" Change Shell To Bash #chsh -s bash

* "chzsh" Change Shell To Zsh #chsh -s zsh